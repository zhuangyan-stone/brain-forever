// CreateUser creates a new user.
// nickname is the default nickname (max 38 chars), rawPassword is the raw password.
// sn is auto-generated via toolset.GenerateSN("u-"), salt is auto-generated randomly,
// password is automatically computed as MD5(rawPassword + salt).
func (s *UserStore) CreateUser(nickname, rawPassword string) (*User, error) {
	// Validate nickname length
	if len(nickname) > 38 {
		return nil, fmt.Errorf(i18n.T("err_nickname_too_long", map[string]any{"Max": 38, "Got": len(nickname)}))
	}
	if len(nickname) == 0 {
		return nil, fmt.Errorf(i18n.T("err_nickname_empty"))
	}

	// Validate password
	if len(rawPassword) == 0 {
		return nil, fmt.Errorf(i18n.T("err_password_empty"))
	}

	sn := toolset.GenerateSN("u-")
	no := s.generateUserNO()
	if no == "" {
		return nil, fmt.Errorf("failed to generate unique user number")
	}
	salt := generateSalt()
	password := encryptPassword(rawPassword, salt)

	result, err := s.db.Exec(
		"INSERT INTO users(nickname, no, sn, salt, password, tel) VALUES(?, ?, ?, ?, ?, ?)",
		nickname, no, sn, salt, password, "",
	)
	if err != nil {
		// Check for sn duplicate
		if strings.Contains(err.Error(), "UNIQUE constraint failed") {
			return nil, fmt.Errorf("duplicate sn: %w", err)
		}
		return nil, fmt.Errorf("%s: %w", i18n.T("db_create_user_failed"), err)
	}

	id, _ := result.LastInsertId()
	return s.GetUserByID(id)
}

// GetUserByID retrieves a user by ID
func (s *UserStore) GetUserByID(id int64) (*User, error) {
	var u User
	err := s.db.Get(&u, "SELECT id, nickname, no, sn, salt, password, tel, create_at, update_at FROM users WHERE id = ?", id)
	if err != nil {
		if err == sql.ErrNoRows {
			return nil, fmt.Errorf("%s (id=%d)", i18n.T("db_user_not_found_by_id"), id)
		}
		return nil, fmt.Errorf("%s: %w", i18n.T("db_query_user_failed"), err)
	}
	return &u, nil
}

// GetUserByNO retrieves a user by no (user number)
