// ============================================================
// User struct
// ============================================================

// User represents user information
type User struct {
	ID       int64  `db:"id"`        // Integer, locally unique (auto-increment primary key)
	No       string `db:"no"`        // Fixed 6-character user number
	SN       string `db:"sn"`        // Unique user serial number generated by this program
	Tel      string `db:"tel"`       // Phone number, max 18 chars, NOT NULL default '' (empty = unverified)
	Nickname string `db:"nickname"`  // Default nickname, max 38 characters
	Password string `db:"password"`  // Password: MD5 of raw password + salt
	Salt     string `db:"salt"`      // Salt for password encryption (random hex string)
	Deleted  int    `db:"deleted"`   // Soft delete flag, 0 = active, 1 = deleted
	CreateAt string `db:"create_at"` // Creation time, defaults to current time
	UpdateAt string `db:"update_at"` // Last update time, defaults to current time on creation, auto-updated on modification
}

// ============================================================
// UserStore
// ============================================================

// UserStore manages user storage
type UserStore struct {
	db *sqlx.DB
}

// OpenUserStore opens the users database and ensures its schema.
// The first actual connection is established lazily on the first query.
// Caller MUST call Close when done.
func OpenUserStore(dbPath string) (*UserStore, error) {
	db, err := sqlx.Open("sqlite3", dbPath+"?_journal_mode=WAL&_busy_timeout=5000&_foreign_keys=1")
	if err != nil {
		return nil, fmt.Errorf("%s: %w", i18n.T("db_open_user_db_failed"), err)
	}
	store := &UserStore{db: db}
	if err := store.initSchema(); err != nil {
		return nil, err
	}
	return store, nil
}

// NewUserStore creates a new UserStore.
// dbPath is the path to user.db (e.g., "./user.db").
// Deprecated: Use OpenUserStore instead for on-demand open/close pattern.
func NewUserStore(dbPath string) (*UserStore, error) {
	return OpenUserStore(dbPath)
}

// initSchema initializes the user table schema.
func (s *UserStore) initSchema() error {
	schema := `
		CREATE TABLE IF NOT EXISTS users (
			id        INTEGER PRIMARY KEY AUTOINCREMENT,
			no        TEXT    NOT NULL CHECK(length(no) = 6),
			sn        TEXT    NOT NULL CHECK(length(sn) <= 58),
			tel       TEXT    NOT NULL DEFAULT '' CHECK(length(tel) <= 18),
			nickname  TEXT    NOT NULL CHECK(length(nickname) <= 38),
			password  TEXT    NOT NULL,
			salt      TEXT    NOT NULL CHECK(length(salt) = 32),
			deleted   INTEGER NOT NULL DEFAULT 0,
			create_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
			update_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
		);

		CREATE UNIQUE INDEX IF NOT EXISTS idx_users_no
			ON users(no);

		CREATE UNIQUE INDEX IF NOT EXISTS idx_users_sn
			ON users(sn);

		CREATE TRIGGER IF NOT EXISTS trg_users_update_at
			BEFORE UPDATE ON users
			FOR EACH ROW
		BEGIN
			UPDATE users SET update_at = CURRENT_TIMESTAMP WHERE id = NEW.id;
		END;
	`
	_, err := s.db.Exec(schema)
	if err != nil {
		return fmt.Errorf("%s: %w", i18n.T("db_init_user_table_failed"), err)
	}
	return nil
}

// ============================================================
// Password utility functions
// ============================================================

// md5Hash computes the MD5 hash and returns a 32-character lowercase hex string
func md5Hash(s string) string {
	return fmt.Sprintf("%x", md5.Sum([]byte(s)))
}

// encryptPassword encrypts the raw password using salt with MD5.
// Rule: MD5(rawPassword + salt)
func encryptPassword(rawPassword, salt string) string {
	return md5Hash(rawPassword + salt)
}

// ============================================================
// Salt generation utilities
// ============================================================

// generateSalt generates a random 32-character hex string for use as a password salt.
func generateSalt() string {
	b := make([]byte, 16) // 16 bytes -> 32 hex chars
	if _, err := rand.Read(b); err != nil {
		// Fallback: should never happen on modern OS
		return fmt.Sprintf("%x", md5.Sum([]byte(fmt.Sprintf("%d", time.Now().UnixNano()))))
	}
	return fmt.Sprintf("%x", b)
}

// generateUserNO generates a unique 6-character user number.
// Format: 1 uppercase letter (excluding I, O, Z) + 5 random digits.
// Checks DB for duplicates and retries up to 5 times.
// Returns empty string if all attempts fail due to collisions.
func (s *UserStore) generateUserNO() string {
	const letters = "ABCDEFGHJKLMNPQRSTUVWXY" // A-Z excluding I, O, Z (23 letters)

	for attempt := 0; attempt < 5; attempt++ {
		// Pick a random letter
		lb := make([]byte, 1)
		rand.Read(lb)
		letter := letters[int(lb[0])%len(letters)]

		// Generate 5 random digits
		db := make([]byte, 5)
		rand.Read(db)
		digits := fmt.Sprintf("%05d",
			int(db[0])%10*10000+
				int(db[1])%10*1000+
				int(db[2])%10*100+
				int(db[3])%10*10+
				int(db[4])%10)

		candidate := string(letter) + digits

		// Check if already exists (uses unique index, no full scan)
		var exists int
		err := s.db.Get(&exists, "SELECT EXISTS(SELECT 1 FROM users WHERE no = ?)", candidate)
		if err != nil {
			continue // DB error, try again
		}
		if exists == 0 {
			return candidate
		}
	}
	return "" // All 5 attempts failed due to collisions
}

