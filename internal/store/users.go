// VerifyPassword verifies if the user's password is correct.
// rawPassword is the raw password to verify; it computes MD5 with the user's salt and compares.
func (s *UserStore) VerifyPassword(sn, rawPassword string) (bool, error) {
	u, err := s.GetUserBySN(sn)
	if err != nil {
		return false, err
	}
	return u.Password == encryptPassword(rawPassword, u.Salt), nil
}

// UpdateNickname updates the user's nickname
func (s *UserStore) UpdateNickname(id int64, newNickname string) error {
	if len(newNickname) > 38 {
		return fmt.Errorf(i18n.T("err_nickname_too_long", map[string]any{"Max": 38, "Got": len(newNickname)}))
	}
	if len(newNickname) == 0 {
		return fmt.Errorf(i18n.T("err_nickname_empty"))
	}

	result, err := s.db.Exec("UPDATE users SET nickname = ? WHERE id = ?", newNickname, id)
	if err != nil {
		return fmt.Errorf("%s: %w", i18n.T("db_update_nickname_failed"), err)
	}
	rows, _ := result.RowsAffected()
	if rows == 0 {
		return fmt.Errorf("%s (id=%d)", i18n.T("db_user_not_found_by_id"), id)
	}
	return nil
}

// UpdatePassword updates the user's password.
// Automatically re-computes MD5 using the existing salt.
func (s *UserStore) UpdatePassword(id int64, newRawPassword string) error {
	if len(newRawPassword) == 0 {
		return fmt.Errorf("password cannot be empty")
	}

	// First get user info to obtain salt
	u, err := s.GetUserByID(id)
	if err != nil {
		return err
	}

	newPassword := encryptPassword(newRawPassword, u.Salt)
	_, err = s.db.Exec("UPDATE users SET password = ? WHERE id = ?", newPassword, id)
	if err != nil {
		return fmt.Errorf("%s: %w", i18n.T("db_update_password_failed"), err)
	}
	return nil
}

// DeleteUser deletes a user.
func (s *UserStore) DeleteUser(id int64) error {
	result, err := s.db.Exec("DELETE FROM users WHERE id = ?", id)
	if err != nil {
		return fmt.Errorf("%s: %w", i18n.T("db_delete_user_failed"), err)
	}
	rows, _ := result.RowsAffected()
	if rows == 0 {
		return fmt.Errorf("%s (id=%d)", i18n.T("db_user_not_found_by_id"), id)
	}
	return nil
}

// ListUsers lists all users
func (s *UserStore) ListUsers() ([]User, error) {
	var users []User
	err := s.db.Select(&users, "SELECT id, nickname, no, sn, salt, password, tel, create_at, update_at FROM users ORDER BY id")
	if err != nil {
		return nil, fmt.Errorf("%s: %w", i18n.T("db_list_users_failed"), err)
	}
	return users, nil
}

